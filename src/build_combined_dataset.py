"""
build_combined_dataset.py
=========================
Merge the Roboflow player set + the converted PadelTracker100 set into one
YOLO dataset using HARD LINKS (instant, no extra disk, same filesystem).

Result:
    data/datasets/combined/{images,labels}/{train,val}/...
    data/datasets/combined/data.yaml

A hard link is a real file, so Ultralytics' label discovery
(images/foo.jpg -> labels/foo.txt) works correctly.

Usage:
    python src/build_combined_dataset.py
"""

from __future__ import annotations

import os
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA = PROJECT_ROOT / "data" / "datasets"
OUT = DATA / "combined"

# (image_dir, label_dir) per split. Order doesn't matter.
SOURCES = {
    "train": [
        (DATA / "players" / "train" / "images", DATA / "players" / "train" / "labels"),
        (DATA / "padeltracker100_yolo" / "images" / "train",
         DATA / "padeltracker100_yolo" / "labels" / "train"),
    ],
    "val": [
        (DATA / "players" / "valid" / "images", DATA / "players" / "valid" / "labels"),
        (DATA / "padeltracker100_yolo" / "images" / "val",
         DATA / "padeltracker100_yolo" / "labels" / "val"),
    ],
}


def hardlink_or_copy(src: Path, dst: Path) -> None:
    if dst.exists():
        return
    try:
        os.link(src, dst)             # hard link (fast, no extra space)
    except OSError:
        import shutil
        shutil.copy2(src, dst)        # fallback across filesystems


def link_split(split: str, img_dirs, lbl_dirs) -> int:
    out_img = OUT / "images" / split
    out_lbl = OUT / "labels" / split
    out_img.mkdir(parents=True, exist_ok=True)
    out_lbl.mkdir(parents=True, exist_ok=True)

    # namespace files per source to avoid name collisions across datasets
    n = 0
    for img_dir, lbl_dir in zip(img_dirs, lbl_dirs):
        if not img_dir.exists():
            print(f"  [skip] {img_dir} (missing)")
            continue
        tag = img_dir.parents[1].name + "_" + img_dir.parents[0].name  # unique-ish prefix
        for img in img_dir.iterdir():
            if img.suffix.lower() not in (".jpg", ".jpeg", ".png"):
                continue
            lbl = lbl_dir / (img.stem + ".txt")
            stem = f"{tag}__{img.stem}"
            hardlink_or_copy(img, out_img / f"{stem}{img.suffix}")
            if lbl.exists():
                hardlink_or_copy(lbl, out_lbl / f"{stem}.txt")
            n += 1
    return n


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    counts = {}
    for split, pairs in SOURCES.items():
        img_dirs = [p[0] for p in pairs]
        lbl_dirs = [p[1] for p in pairs]
        counts[split] = link_split(split, img_dirs, lbl_dirs)
        print(f"[{split}] linked {counts[split]} image/label pairs")

    yaml = f"""# Auto-generated by src/build_combined_dataset.py
# Combined: Roboflow (octovar/player-padel-dataset) + PadelTracker100 (Zenodo)
path: {OUT}
train: images/train
val: images/val
test: images/val
nc: 1
names:
  0: player
"""
    (OUT / "data.yaml").write_text(yaml)
    print(f"[data.yaml] written -> {OUT/'data.yaml'}")
    print(f"TOTAL: train={counts['train']} val={counts['val']}")


if __name__ == "__main__":
    main()
